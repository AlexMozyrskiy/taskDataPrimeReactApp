export const getIsTextAreaActiveSelector =  (state) => {
    return state.textArea.isTextareaActive;
}

export const getIsPlaceHolderInFirstDivActiveSelector =  (state) => {
    return state.textArea.isPlaceHolderInFirstDivActive;
}

